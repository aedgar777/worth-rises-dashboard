function titleCase(value: string): string {
  return value
    .toLowerCase()
    .split(/\s+/)
    .filter(Boolean)
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(" ");
}

export function getStateName(abbr: string): string {
  return STATE_NAMES[abbr.toUpperCase()] ?? abbr.toUpperCase();
}

export function formatCountyName(county: string): string {
  const cleaned = county.replace(/\s+County$/i, "").trim();
  return `${titleCase(cleaned)} County`;
}

export function formatJurisdiction(row: {
  jurisdiction_type: string;
  state: string;
  county: string | null;
}): string {
  if (row.jurisdiction_type === "state") {
    return getStateName(row.state);
  }

  if (row.county) {
    return `${formatCountyName(row.county)}, ${getStateName(row.state)}`;
  }

  return getStateName(row.state);
}
